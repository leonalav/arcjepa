import json
import torch
import numpy as np
from pathlib import Path
from typing import List, Dict, Any, Optional
import random

# Discover project root (3 levels up from src/data/dataset.py)
PROJECT_ROOT = Path(__file__).parent.parent.parent.resolve()

# Official ARC-AGI imports
import arc_agi
from torch.utils.data import Dataset
from arcengine import GameAction, GameState

class ARCTrajectoryDataset(Dataset):
    """
    Dataset for ARC-AGI-3 game trajectories.
    Parses JSONL recordings generated by the official ARC-AGI Toolkit and 
    provides sliding window chunks of (state, action) pairs.
    """
    def __init__(
        self,
        recording_files: List[str],
        window_size: int = 24,
        stride: int = 4,
        max_grid_size: int = 64,  # Updated to 64 based on ARC-AGI-3 docs
        multistep_k: int = 1,
        compute_temporal_masks: bool = False,
        filter_noops: bool = False
    ):
        self.window_size = window_size
        self.stride = stride
        self.max_grid_size = max_grid_size
        self.multistep_k = multistep_k
        self.compute_temporal_masks = compute_temporal_masks
        self.filter_noops = filter_noops
        self.chunks = []

        self._load_recordings(recording_files)

    def _load_recordings(self, recording_files: List[str]):
        for file_path in recording_files:
            trajectory = []
            with open(file_path, 'r') as f:
                for line in f:
                    if not line.strip():
                        continue
                    frame_data = json.loads(line)
                    trajectory.append(self._preprocess_frame(frame_data))

            # Chunk the trajectory into sliding windows
            if len(trajectory) < 2: # Need at least (s_t, a_t, s_{t+1})
                continue

            for i in range(0, len(trajectory) - self.window_size, self.stride):
                chunk = trajectory[i : i + self.window_size + 1] # +1 for target state

                # Filter no-op chunks (not trajectories) if requested
                if self.filter_noops and self._is_noop_chunk(chunk):
                    continue

                self.chunks.append(chunk)

    def _preprocess_frame(self, frame_data: Dict[str, Any]) -> Dict[str, Any]:
        # Extract grid (Official recordings store this in the 'frame' key)
        grid = np.array(frame_data.get('frame', []), dtype=np.int64)
        
        # Handle cases where the frame might be empty/None initially
        if grid.size == 0:
            grid = np.zeros((1, 1), dtype=np.int64)
            
        h, w = grid.shape
        
        # Safety crop just in case the environment violates the 64x64 max
        safe_h, safe_w = min(h, self.max_grid_size), min(w, self.max_grid_size)
        
        # Pad grid to max_grid_size
        padded_grid = np.zeros((self.max_grid_size, self.max_grid_size), dtype=np.int64)
        padded_grid[:safe_h, :safe_w] = grid[:safe_h, :safe_w]
        
        # Action parsing (Compliant with Official arcengine format)
        action_data = frame_data.get('action_input', {})
        if isinstance(action_data, str):
            action_type = action_data
            ax, ay = 0, 0
        else:
            action_type = action_data.get('action', 'NONE')
            ax = action_data.get('x', 0)
            ay = action_data.get('y', 0)
            
        # Map action type to index (ACTION1-7 -> 1-7, SUBMIT -> 8, others 0)
        action_idx = 0
        if action_type.startswith('ACTION') and action_type[6:].isdigit():
            action_idx = int(action_type[6:])
        elif action_type == 'SUBMIT':
            action_idx = 8
        
        return {
            'grid': torch.from_numpy(padded_grid),
            'action_type': action_idx,
            'x': ax,
            'y': ay,
            'grid_h': safe_h,
            'grid_w': safe_w
        }

    def __len__(self):
        return len(self.chunks)

    def __getitem__(self, idx):
        chunk = self.chunks[idx]

        grids = torch.stack([step['grid'] for step in chunk])
        action_types = torch.tensor([step['action_type'] for step in chunk[:-1]], dtype=torch.long)
        xs = torch.tensor([step['x'] for step in chunk[:-1]], dtype=torch.long)
        ys = torch.tensor([step['y'] for step in chunk[:-1]], dtype=torch.long)

        # Input: steps 0 to T-1 (grids and actions)
        # Target: steps 1 to T (grids for latent prediction)
        result = {
            'states': grids[:-1],          # [T, 64, 64]
            'actions': action_types,        # [T]
            'coords_x': xs,                 # [T]
            'coords_y': ys,                 # [T]
            'target_states': grids[1:],     # [T, 64, 64]
            'final_state': grids[-1]        # [64, 64] (for reconstruction loss)
        }

        # Compute temporal masks if requested
        if self.compute_temporal_masks:
            temporal_mask = self._compute_temporal_mask(grids[:-1], grids[1:])
            result['temporal_mask'] = temporal_mask

        return result

    def _compute_temporal_mask(self, states: torch.Tensor, target_states: torch.Tensor) -> torch.Tensor:
        """
        Compute binary mask indicating which pixels changed between consecutive states.

        Args:
            states: [T, H, W] input states
            target_states: [T, H, W] target states

        Returns:
            mask: [T, H, W] binary mask (1 = changed, 0 = unchanged)
        """
        return (states != target_states).float()

    def _is_noop_chunk(self, chunk: List[Dict]) -> bool:
        """
        Check if a specific chunk consists entirely of no-op transitions (all grids identical).

        Args:
            chunk: List of preprocessed frames in this sliding window

        Returns:
            True if all grids in chunk are identical, False otherwise
        """
        if len(chunk) < 2:
            return True

        first_grid = chunk[0]['grid']
        for step in chunk[1:]:
            if not torch.equal(first_grid, step['grid']):
                return False

        return True

    def validate_arc_compliance(self):
        """Validate dataset respects ARC-AGI-3 specifications"""
        print(f"Validating {len(self.chunks)} chunks for ARC-AGI-3 compliance...")

        for chunk_idx, chunk in enumerate(self.chunks):
            for step_idx, step in enumerate(chunk):
                grid = step['grid']

                # Check grid size
                assert grid.shape == (64, 64), \
                    f"Chunk {chunk_idx}, Step {step_idx}: Invalid grid shape {grid.shape}"

                # Check color range
                assert grid.min() >= 0 and grid.max() <= 15, \
                    f"Chunk {chunk_idx}, Step {step_idx}: Invalid color range [{grid.min()}, {grid.max()}]"

                # Check action type
                assert 0 <= step['action_type'] <= 8, \
                    f"Chunk {chunk_idx}, Step {step_idx}: Invalid action type {step['action_type']}"

                # Check coordinates
                assert 0 <= step['x'] < 64 and 0 <= step['y'] < 64, \
                    f"Chunk {chunk_idx}, Step {step_idx}: Invalid coords ({step['x']}, {step['y']})"

        print(f"✓ Dataset validation passed: {len(self.chunks)} chunks comply with ARC-AGI-3 specs")

def create_mock_trajectory(output_dir: str, num_trajectories: int = 5):
    """
    Utility to create authentic ARC-AGI-3 JSONL trajectories.
    Instead of hallucinating data, we use the official engine to play a random game
    and leverage its built-in recording functionality.
    """
    Path(output_dir).mkdir(parents=True, exist_ok=True)
    
    # Define a local directory for ARC environments
    env_dir = PROJECT_ROOT / "data" / "arc_environments"
    env_dir.mkdir(parents=True, exist_ok=True)
    
    # Instantiate the official Arcade entry point
    arc = arc_agi.Arcade(environments_dir=str(env_dir))
    
    # Standard ARC-AGI-3 beginner games
    game_ids = ["ls20", "v750", "d963", "t432", "b210"]
    
    # Standard ARC-AGI-3 beginner games available in your environment
    game_ids = ["ls20", "ar25", "bp35", "cd82", "cn04", "dc22", "ft09", "wa30"]
    
    print(f"Generating {num_trajectories} trajectories across {len(game_ids)} games...")
    
    for i in range(num_trajectories):
        game_id = game_ids[i % len(game_ids)]
        # By setting save_recording=True, the engine natively dumps the JSONL we need
        env = arc.make(game_id, save_recording=True)
        obs = env.reset()
        
        # Play random actions until completion or timeout
        max_steps = 50
        step = 0
        
        while step < max_steps:
            # Pick a random action type
            action_enum = random.choice([
                GameAction.ACTION1, GameAction.ACTION2, GameAction.ACTION3, 
                GameAction.ACTION4, GameAction.ACTION5, GameAction.ACTION6
            ])
            
            # If the action requires coordinates, provide them via the 'data' kwarg
            if action_enum == GameAction.ACTION6:
                obs = env.step(action_enum, data={"x": random.randint(0, 30), "y": random.randint(0, 30)})
            else:
                obs = env.step(action_enum)
                
            if obs and obs.state in [GameState.WIN, GameState.GAME_OVER]:
                break
            step += 1
            
        print(f"Trajectory {i+1} ({game_id}) completed.")

    # ARC-AGI saves recordings in its own scorecard directory. 
    # We need to move them to the output_dir so our Dataset can find them.
    import shutil
    # The default location is usually under the current user's home or a relative 'recordings' dir
    # Based on your log, it's relative to the run directory
    local_recordings = Path("recordings")
    if local_recordings.exists():
        for jsonl_file in local_recordings.glob("**/*.jsonl"):
            shutil.copy(jsonl_file, Path(output_dir) / jsonl_file.name)
        print(f"Moved generated recordings to {output_dir}")
    else:
        print("Warning: Could not find generated recordings to move.")